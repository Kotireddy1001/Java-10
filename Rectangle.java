public class Rectangle {
    public static void main(String[] args) {
        double length = 7.5;
        double breadth = 4.2;

        double area = length * breadth;
        double perimeter = 2 * (length + breadth);

        System.out.println("Area: " + area);
        System.out.println("Perimeter: " + perimeter);
    }
}
