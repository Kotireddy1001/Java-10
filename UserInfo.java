//User Info Display

public class UserInfo {
    public static void main(String[] args) {
        String name = "Anjali";
        int age = 25;
        double height = 5.4;
        char gender = 'F';

        System.out.println("Name: " + name);
        System.out.println("Age: " + age);
        System.out.println("Height: " + height + " ft");
        System.out.println("Gender: " + gender);
    }
}
